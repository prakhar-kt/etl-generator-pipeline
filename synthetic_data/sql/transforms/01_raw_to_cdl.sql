-- ─── CDL Dimension: Company ────────────────────────────────
MERGE INTO CDL_NovaStar.cdl_dim_company T
USING (
  SELECT
    FARM_FINGERPRINT(COMPANY_CODE) AS KEY_COMPANY,
    COMPANY_CODE,
    COMPANY_NAME,
    REGION,
    CURRENCY_CODE,
    GEOGRAPHIC_SECURITY_CODE,
    COUNTRY,
    STATUS,
    LOAD_DATE AS RAW_LOAD_DATE
  FROM Src_NovaStar.raw_company
) S
ON T.KEY_COMPANY = S.KEY_COMPANY
WHEN MATCHED THEN UPDATE SET
  T.COMPANY_NAME = S.COMPANY_NAME,
  T.REGION = S.REGION,
  T.CURRENCY_CODE = S.CURRENCY_CODE,
  T.GEOGRAPHIC_SECURITY_CODE = S.GEOGRAPHIC_SECURITY_CODE,
  T.COUNTRY = S.COUNTRY,
  T.STATUS = S.STATUS,
  T.RAW_LOAD_DATE = S.RAW_LOAD_DATE,
  T.CDL_LOAD_DATE = CURRENT_TIMESTAMP()
WHEN NOT MATCHED THEN INSERT (
  KEY_COMPANY, COMPANY_CODE, COMPANY_NAME, REGION, CURRENCY_CODE,
  GEOGRAPHIC_SECURITY_CODE, COUNTRY, STATUS, RAW_LOAD_DATE, CDL_LOAD_DATE
) VALUES (
  S.KEY_COMPANY, S.COMPANY_CODE, S.COMPANY_NAME, S.REGION, S.CURRENCY_CODE,
  S.GEOGRAPHIC_SECURITY_CODE, S.COUNTRY, S.STATUS, S.RAW_LOAD_DATE, CURRENT_TIMESTAMP()
);

