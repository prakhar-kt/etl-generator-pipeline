-- ─── CDL Dimension: Selling Method ─────────────────────────
MERGE INTO CDL_NovaStar.cdl_dim_selling_method T
USING (
  SELECT
    FARM_FINGERPRINT(code) AS KEY_SELLING_METHOD,
    code AS SELLING_METHOD_CODE,
    name AS SELLING_METHOD_NAME
  FROM UNNEST([
    STRUCT('100' AS code, 'Wholesale' AS name),
    STRUCT('200', 'Direct-to-Consumer'),
    STRUCT('300', 'E-Commerce'),
    STRUCT('400', 'Marketplace')
  ])
) S
ON T.KEY_SELLING_METHOD = S.KEY_SELLING_METHOD
WHEN NOT MATCHED THEN INSERT (
  KEY_SELLING_METHOD, SELLING_METHOD_CODE, SELLING_METHOD_NAME, CDL_LOAD_DATE
) VALUES (
  S.KEY_SELLING_METHOD, S.SELLING_METHOD_CODE, S.SELLING_METHOD_NAME, CURRENT_TIMESTAMP()
);

-- ─── CDL Dimension: Calendar ───────────────────────────────
MERGE INTO CDL_NovaStar.cdl_dim_calendar T
USING (
  SELECT
    FARM_FINGERPRINT(CONCAT(CAST(EXTRACT(YEAR FROM d) AS STRING), CAST(d AS STRING))) AS KEY_CALENDAR,
    d AS CALENDAR_DATE,
    EXTRACT(YEAR FROM d) AS YEAR,
    EXTRACT(MONTH FROM d) AS MONTH,
    EXTRACT(QUARTER FROM d) AS QUARTER,
    EXTRACT(ISOWEEK FROM d) AS WEEK_OF_YEAR,
    EXTRACT(DAYOFWEEK FROM d) AS DAY_OF_WEEK,
    EXTRACT(DAYOFWEEK FROM d) IN (1, 7) AS IS_WEEKEND
  FROM UNNEST(GENERATE_DATE_ARRAY('2024-01-01', '2025-12-31')) AS d
) S
ON T.KEY_CALENDAR = S.KEY_CALENDAR
WHEN NOT MATCHED THEN INSERT (
  KEY_CALENDAR, CALENDAR_DATE, YEAR, MONTH, QUARTER,
  WEEK_OF_YEAR, DAY_OF_WEEK, IS_WEEKEND, CDL_LOAD_DATE
) VALUES (
  S.KEY_CALENDAR, S.CALENDAR_DATE, S.YEAR, S.MONTH, S.QUARTER,
  S.WEEK_OF_YEAR, S.DAY_OF_WEEK, S.IS_WEEKEND, CURRENT_TIMESTAMP()
);

